/*
    RELATIONAL OPERATORS
    <
    <=
    >
    >=
    ==
    !=

    all this would give true/false


    LOGICAL

    OR(||) AND(&&) NOT(!)
*/



#include <iostream>
using namespace std;

int main(){
    cout<< !(3<5)<<endl; //not(true)-->false-->0
    cout<< (3<5)<<endl; //true-->1
    cout<<(3>5)<<endl;  //false-->0
    cout<<(3<=3)<<endl; //true-->1
    cout<<(3!=5)<<endl; //true-->1


    return 0;
}